# Take two numbers from the user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))


# Perform basic math calculations
print("**********************************************************************************************")
add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2
rem = num1 % num2
pwr = num1 ** num2

# Print the results
print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)
print("Modulus:", rem)
print("Exponent:", pwr)
print("*********************************************************************************************")